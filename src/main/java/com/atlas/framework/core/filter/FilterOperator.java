package com.atlas.framework.core.filter;

public enum FilterOperator {
    EQ,         // equals
    NE,         // not equals
    GT,         // greater than
    LT,         // less than
    GTE,        // >=
    LTE,        // <=
    LIKE,       // contains
    STARTS_WITH,
    ENDS_WITH,
    IN,
    NOT_IN
}
